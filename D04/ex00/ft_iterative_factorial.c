int		ft_iterative_factorial(int nb)
{
	int i;
	int nbr;

	i = 1;
	nbr = 1;
	if (nb > 0 && nb < 13)
	{
		while (i <= nb)
		{
			nbr *= i;
			i++;
		}
	}
	else
	{
		return (0);
	}
	return (nbr);
}
