#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

int main(void)
{
	int i;

	i = 9;
	while (i >= 0)
	{
		putchar(i + '0');
		i--;
	}
	putchar('\n');
	return (0);
}
