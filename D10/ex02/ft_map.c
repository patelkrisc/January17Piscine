#include <stdlib.h>

int		*ft_map(int *tab, int length, int (*f)(int))
{
	int		i;
	int		*j;

	i = 0;
	j = (int*)malloc(sizeof(*j) * (length + 1));
	while (i < length)
	{
		j[i] = f(tab[i]);
		i++;
	}
	return (j);
}
