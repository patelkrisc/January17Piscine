#include <stdlib.h>

char	rm_spaces(char *s1)
{
	int		a;
	int		b;
	char	*s2;

	a = 0;
	b = 0;
	s2 = malloc(ft_strlen(s1) + 1);
	while (s1[a] != '\0')
	{
		if (s1[a] != ' ')
		{
			s2[b] == s1[a];
			b += 1;
		}
		a += 1;
	}
	s2[b] = '\0';
	return (s2[b]);
}
